package com.ferdinurul.mysubmissionpemula.models;

public class ModelData {
    private String nama_konten;

    private String kategori;

    private String deskripsi;

    private int konten;

    public String getNama_konten() {
        return nama_konten;
    }

    public void setNama_konten(String nama_konten) {
        this.nama_konten = nama_konten;
    }

    public String getKategori() {
        return kategori;
    }

    public void setKategori(String kategori) {
        this.kategori = kategori;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }

    public int getKonten() {
        return konten;
    }

    public void setKonten(int konten) {
        this.konten = konten;
    }
}
